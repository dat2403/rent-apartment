import {toast} from "react-toastify";

export const showSuccessToast = (mess: string) => {
  return toast.success(mess, {
    position: "top-right",
    autoClose: 2000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
  });
}


